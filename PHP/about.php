<h1 class="about">Our History</h1>


<p class="about">The Historical Society of Eastern Virginia was established in 1998 by a small group dedicated to preserving our past. In 2005, with the help of our generous sponsors, we were able to open the New Pine Museum. In 2011, we were able to restore the Williams House on Flowers Street, thanks in part to our team of volunteers.</p>

<h2 class="about">Our Mission</h2>
<p class="about">We are a non-profit organization dedicated to the promotion, continued education, and preservation of Virginia's history. We strive to make history exciting and engaging for everyone of all ages. We also strive to restore historical homes within New Pine and restore them to their former glory.</p>
<h2 class="about">Our Vision</h2>
<p class="about">As the head historical organization in New Pine, we endeavor to preserve our community's history. To assist in doing so, our board has monthly meetings to discuss business and develop new programs to promote historical education. These meetings are open to the public. In addition, we also have an annual meeting and dinner exclusively for members.
<h2 class="about">Our Goals</h2>

<p class="about">This year we are in the process of restoring the Anderson House on Hamilton Street. Built in 1816 by James Anderson, this Federal Style home was the home of Mayor Reginald Banks where he resided until his death in 1858. The home was then purchased by the Wellington family where it was passed down through the generations and now is in our hands, to be properly restored.
To further protect our collections, we are holding a fundraiser this year to build an improved climate-controlled storage facility. This will help to preserve precious documents from the elements.</p>
